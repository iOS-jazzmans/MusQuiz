import UIKit

extension UIFont {
    
    static func buttonTitle() -> UIFont? {
        return UIFont.init(name: "Qanelas-Medium", size: 20)
    }
    
    static func primaryText() -> UIFont? {
        return UIFont.init(name: "Qanelas-Medium", size: 22)
    }
    
}
